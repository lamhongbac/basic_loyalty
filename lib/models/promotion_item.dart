class PromotionItem {
  final String title;      // The title of the promotion
  final String subtitle;   // The subtitle or offer description
  final String imageUrl;   // The image URL or asset path for the promotion image
  final String brand;      // The name of the brand associated with the promotion

  PromotionItem({
    required this.title,
    required this.subtitle,
    required this.imageUrl,
    required this.brand,
  });
}
